// –î–æ–±–∞–≤–∏—Ç—å –∏–≥—Ä—É –Ω–∞ –¥–æ—Å–∫—É
app.post('/api/user/boards/:boardId/games', authenticateToken, (req, res) => {
  try {
    const { boardId } = req.params;
    const { game } = req.body;

    if (!userBoards[req.user.id]) {
      userBoards[req.user.id] = {
        backlog: [],
        playing: [],
        completed: [],
        dropped: []
      };
    }

    if (!userBoards[req.user.id][boardId]) {
      return res.status(400).json({ error: '–ù–µ–≤–µ—Ä–Ω–∞—è –¥–æ—Å–∫–∞' });
    }

    const card = {
      id: `game-${Date.now()}`,
      gameId: game.id,
      name: game.name,
      cover: game.cover,
      rating: null,
      notes: '',
      addedDate: new Date().toISOString()
    };

    userBoards[req.user.id][boardId].push(card);

    res.json({ message: '–ò–≥—Ä–∞ –¥–æ–±–∞–≤–ª–µ–Ω–∞', card });
  } catch (error) {
    console.error('–û—à–∏–±–∫–∞ –¥–æ–±–∞–≤–ª–µ–Ω–∏—è –∏–≥—Ä—ã:', error);
    res.status(500).json({ error: '–û—à–∏–±–∫–∞ —Å–µ—Ä–≤–µ—Ä–∞' });
  }
});

